import { Article } from '@packages/client/src/components/Article'
import { Pages } from '@packages/client/src/components/Pages'
import styles from '@packages/client/src/styles/Home.module.css'
import { trpc } from '@packages/client/src/utils/trpc'
import { ArticleFiltersDTO, ZodPagination } from '@packages/server'
import Head from 'next/head'
import Link from 'next/link'
import { useRouter } from 'next/router'

export default function SearchPage() {
  const router = useRouter()

  const filters = ArticleFiltersDTO.parse(router.query)
  const page = ZodPagination.parse(router.query)

  const articlesQuery = trpc.articles.getMany.useQuery({ filters, pagination: page }).data

  if (!articlesQuery) {
    return <div>Loading...</div>
  }

  return (
    <>
      <Head>
        <title>Realworld App!</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <Link href={'/'}>In√≠cio</Link>
        <a href={'/search'}>
          <h1>Search</h1>
        </a>
        <ul>
          {articlesQuery &&
            articlesQuery.articles.map(article => (
              <li key={article.slug} className={styles.content}>
                <Article {...article} />
              </li>
            ))}
        </ul>
        {articlesQuery && <Pages pagination={page} totalInPage={articlesQuery.articles.length} />}
      </main>
    </>
  )
}
